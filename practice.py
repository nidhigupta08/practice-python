"""Round Practice #1
 Round the result of the division 10/3 to a number with 2 decimal places, and print the rounded value on the screen"""
value = round(10 / 3, 2)
print(value)

"""Round Practice #2
Round the number 10.676767 to the nearest integer, and print the result."""
num = 10.676767
result = round(num)
print(result)

