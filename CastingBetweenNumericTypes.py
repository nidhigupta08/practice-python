
# Initial integer value
integer_value = 42

# Convert integer to float
float_value = float(integer_value)
print(f"The float value is: {float_value}")

# Convert integer to complex
complex_value = complex(integer_value)
print(f"The complex value is: {complex_value}")

# Initial float value
float_value = 3.14

# Convert float to integer
integer_from_float = int(float_value)
print(f"The integer value from float is: {integer_from_float}")

# Convert float to complex
complex_from_float = complex(float_value)
print(f"The complex value from float is: {complex_from_float}")
