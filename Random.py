# Python program for generating random float number
# The Python Random module is a built-in module for generating random integers in Python.
# These numbers occur randomly and does not follow any rules or instructuctions.

# The random.random() function gives a float number that ranges from 0.0 to 1.0.
import random
num=random.random()
print(num)

# The randint() Function
# The random.randint() function generates a random integer from the range of numbers supplied.

# Python program for generating a random integer
import random
num = random.randint(1, 500)
print( num )
